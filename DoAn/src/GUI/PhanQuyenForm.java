    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        String b=txtTimKiem.getText();
        boolean check=false;
        for(int i=0;i<bus.lssp.size();i++){
            if(bus.lssp.get(i).getID().toLowerCase().equals(b.toLowerCase())){
                check=true;
                model.setRowCount(0);
                model.addRow(new Object[]{
                    bus.lssp.get(i).getID(),bus.lssp.get(i).getMaQuyen(), bus.lssp.get(i).getTenQuyen()
                });
                break;
            }
            else if(bus.lssp.get(i).getTenQuyen().toLowerCase().equals(b.toLowerCase())){
                check=true;
                model.setRowCount(0);
                model.addRow(new Object[]{
                    bus.lssp.get(i).getID(),bus.lssp.get(i).getMaQuyen(), bus.lssp.get(i).getTenQuyen()
                });
                break;
            }
        }
        if(check==false){
            JOptionPane.showMessageDialog(rootPane, "Không có Mã quyền hay TênQuyền trong bảng nhóe !");
            model.setRowCount(0);
        }
    }//GEN-LAST:event_btnTimActionPerformed

