    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        PhanQuyen s=new PhanQuyen();
        PhanQuyenBUS bus=new PhanQuyenBUS();
        boolean check=true;
        String b=txtID.getText();
        if(!bus.KiemTraTrungID(b)){
            check=false;
            JOptionPane.showMessageDialog(rootPane, "Trùng Mã ID rầu !!! Vui lòng nhập lại");
            txtID.requestFocus();
        }
        s.setID(b);
        
        String c=txtMaQuyen.getText();
        if(!c.equals("1") && !c.equals("0")){
            check=false;
            JOptionPane.showMessageDialog(rootPane, "Mã quyền chỉ có 0 or 1");
            txtMaQuyen.requestFocus();
        }
        if(c.equals("0")){
             check=false;
            JOptionPane.showMessageDialog(rootPane, "Đã có Phần Quyền 0(BOSS).Chọn cái khác đi nhé!");
            txtMaQuyen.requestFocus();
        }
        
        s.setMaQuyen(c);
        s.setTenQuyen(txtTenQuyen.getText());
        
        
        if(check==true){
            try {
                if(bus.ThemPhanQuyen(s)==true){
                    bus.showResult(model);
                    JOptionPane.showMessageDialog(rootPane, "Thêm quyền thành công!");
                    
                    txtID.setText("");
                    txtMaQuyen.setText("");
                    txtTenQuyen.setText("");
                    txtMaQuyen.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(PhanQuyen.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Thêm không thành cong ! Chắc là sai 1 chỗ gì đó rầu");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        PhanQuyen s=new PhanQuyen();
        int row=tbl.getSelectedRow();
        boolean check=true;
        if(row == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn dòng để sủa");
            check=false;
        }
        boolean check1=true;
        if(check==true){
            s.setID(txtID.getText());
            String c=txtMaQuyen.getText();
            if(!c.equals("1") && !c.equals("0")){
                check1=false;
                JOptionPane.showMessageDialog(rootPane, "Mã quyền chỉ có 0 or 1");
                txtMaQuyen.requestFocus();
            }
            if(c.equals("0")){
                check1=false;
               JOptionPane.showMessageDialog(rootPane, "Đã có Phần Quyền 0(BOSS).Chọn cái khác đi nhé!");
            }

            s.setMaQuyen(c);
            s.setTenQuyen(txtTenQuyen.getText());
            if(check1==true){
                try {
                    if(bus.SuaPhanQuyen(s)==true){
                        model.setRowCount(0);
                        bus.DocPhanQuyen();
                        bus.showTable(model);
                        txtMaQuyen.setText("");
                        txtTenQuyen.setText("");
                        txtID.setText("");
                        txtID.setEnabled(true);
                        txtID.requestFocus();
                    }
                }catch(SQLException ex){
                    Logger.getLogger(PhanQuyen.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(rootPane, "Edit thất bại");
                }
            } 
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row=tbl.getSelectedRow();
        boolean check=true;
        if(row == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn dòng để sủa");
            check=false;
        }
        if(check==true){
            PhanQuyen std = bus.lssp.get(row);
            if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa ?", "WARNING",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try {
                bus.xoavitrids(row);
                bus.XoaPhanQuyen(std);
                txtMaQuyen.setText("");
                txtTenQuyen.setText("");
                txtID.setText("");
                txtID.setEnabled(true);
                txtID.requestFocus();
            } catch (SQLException ex) {
                Logger.getLogger(PhanQuyen.class.getName()).log(Level.SEVERE, null, ex);
            }
            model.removeRow(row);
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Lần sau nghĩ kỹ r hãy ấn nha mạy !!!");
        }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

