    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NCC s=new NCC();
        NCCBUS bus=new NCCBUS();
        boolean check=true;
        String b=txtMaNCC.getText();
        if(!bus.KiemTraTrungMaNCC(b)){
            check=false;
            JOptionPane.showMessageDialog(rootPane, "Trùng Mã NCC rầu !!! Vui lòng nhập lại");
            txtMaNCC.requestFocus();
        }

        s.setMaNCC(b);
        s.setTenNCC(txtTenNCC.getText());
        if(check==true){
            try {
                if(bus.ThemNCC(s)==true){
                    bus.showResult(model);
                    JOptionPane.showMessageDialog(rootPane, "Thêm NCC thành công!");

                    txtMaNCC.setText("");
                    txtTenNCC.setText("");
                    txtMaNCC.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(NCC.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Thêm không thành cong ! Chắc là sai 1 chỗ gì đó rầu");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row=tbl.getSelectedRow();
        boolean check=true;
        if(row == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn dòng để sủa");
            check=false;
        }
        if(check==true){
            NCC std = bus.lsk.get(row);
            if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa ?", "WARNING",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    bus.xoavitrids(row);
                    bus.XoaNCC(std);
                    txtMaNCC.setText("");
                    txtTenNCC.setText("");
                    txtMaNCC.setEnabled(true);
                    txtMaNCC.requestFocus();
                } catch (SQLException ex) {
                    Logger.getLogger(NCC.class.getName()).log(Level.SEVERE, null, ex);
                }
                model.removeRow(row);
                }
            else {
                JOptionPane.showMessageDialog(rootPane, "Lần sau nghĩ kỹ r hãy ấn nha mạy !!!");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        NCC s=new NCC();
        s.setMaNCC(txtMaNCC.getText());
        s.setTenNCC(txtTenNCC.getText());

        int row=tbl.getSelectedRow();
        boolean check=true;
        if(row == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn dòng để sủa");
            check=false;
        }
        if(check==true){
            try {
            if(bus.SuaNCC(s)==true){
                model.setRowCount(0);
                bus.DocNCC();
                bus.showTable(model);
                txtMaNCC.setText("");
                txtTenNCC.setText("");
                txtMaNCC.setEnabled(true);
                txtMaNCC.requestFocus();
            }
            }catch(SQLException ex){
                Logger.getLogger(NCC.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Edit thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

