    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Kho s = new Kho();
        KhoBUS bus=new KhoBUS();
        boolean check=true;
        
        SanPhamBUS sp=new SanPhamBUS();
        sp.DocSP();
        
        String ma=cmbMaSP.getSelectedItem().toString();
        String ten=txtTenSP.getText();
        if(!bus.KiemTraTrungMaSP(ma)){
            check=false;
            JOptionPane.showMessageDialog(rootPane, "Trùng  Mã SP rồi mẹ");
        }
        if(sp.KiemTraTrungMaSP(ma)==true && sp.KiemTraTrungTenSP(ten)==true){
            check=false;
            JOptionPane.showMessageDialog(rootPane, "Mã SP hoặc Tên SP ko tồn tại");
        }
        s.setMaSP(ma);
        s.setTenSP(ten);
        
        try {
            s.setNgayNhap(new SimpleDateFormat("dd/MM/yyyy").parse(txtNgay.getText()));
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(rootPane, "Sai định dạng ngày,\n Định dạng:dd/MM/yyyy");
            txtNgay.requestFocus();
            check=false;
        }
        
        String gia=txtDonGia.getText();
        try {
            Integer.parseInt(gia);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Gía phải là số nguyên");
            txtDonGia.requestFocus();
            check=false;
        }
        s.setDongia(gia.toString());
        
        String sl=txtSoLuong.getText();
        try {
            Integer.parseInt(sl);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Số lượng phải là số nguyên");
            txtSoLuong.requestFocus();
            check=false;
        }
        s.setSl(sl.toString());
        s.setMaLoai(txtMaLoai.getText());
        s.setGiaxuat(txtGiaXuat.getText());
        

        if(check==true){
            try {
                if(bus.ThemSP(s)==true){
                    bus.showResult(model);
                    JOptionPane.showMessageDialog(rootPane, "Thêm SP thành công!");

                    txtTenSP.setText("");
                    txtNgay.setText("");
                    txtDonGia.setText("");
                    txtGiaXuat.setText("");
                    txtSoLuong.setText("");
                    txtMaLoai.setText("");
                } 
            } catch (SQLException ex) {
                Logger.getLogger(Kho.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Thêm không thành cong ! Chắc là sai 1 chỗ gì đó rầu");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row=tbl.getSelectedRow();
        boolean check1=true;
        if(row == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn dòng để sủa");
            check1=false;
        }
        if(check1==true){
            Kho std = bus.lsk.get(row);
            if (JOptionPane.showConfirmDialog(null, "Are you sure?", "WARNING",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    bus.xoavitrids(row);
                    bus.XoaSP(std);

                    txtTenSP.setText("");
                    txtNgay.setText("");
                    txtGiaXuat.setText("");
                    cmbMaSP.setEnabled(true);
                    txtDonGia.setText("");
                    txtSoLuong.setText("");
                    txtMaLoai.setText("");
                } catch (SQLException ex) {
                    Logger.getLogger(Kho.class.getName()).log(Level.SEVERE, null, ex);
                }
                model.removeRow(row);
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Lần sau nghĩ kỹ r hãy ấn nha mạy !!!");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

