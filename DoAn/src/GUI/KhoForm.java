    private void btnXuatEXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatEXActionPerformed
        WritableWorkbook wordbook;
        
        try{
            wordbook = Workbook.createWorkbook(new File("kho.xls"));
            WritableSheet sheet=wordbook.createSheet("kho", 0);
            
            sheet.addCell(new Label(0,0,"Mã SP"));
            sheet.addCell(new Label(1,0,"Tên SP"));
            sheet.addCell(new Label(2,0,"Mã Loại"));
            sheet.addCell(new Label(3,0,"Ngày nhập"));
            sheet.addCell(new Label(4,0,"Giá 1 cái"));
            sheet.addCell(new Label(5,0,"Giá Xuất"));
            sheet.addCell(new Label(6,0,"Số lượng"));
            int row=1;
            for(Kho kh : KhoBUS.lsk){
                sheet.addCell(new Label(0,row,kh.getMaSP()));
                sheet.addCell(new Label(1,row,kh.getTenSP()));
                sheet.addCell(new Label(2,row,kh.getMaLoai()));
                sheet.addCell(new Label(3,row,kh.getNgayNhap().toString()));
                sheet.addCell(new Label(4,row,kh.getDongia()));
                sheet.addCell(new Label(5,row,kh.getGiaxuat()));
                sheet.addCell(new Label(6,row++,kh.getSl()));
            }
            
            wordbook.write();
            wordbook.close();
            JOptionPane.showMessageDialog(null,"Xuất thành công");
        } catch (IOException ex) {
            Logger.getLogger(Kho.class.getName()).log(Level.SEVERE, null, ex);
        } catch (WriteException ex) {
            Logger.getLogger(Kho.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnXuatEXActionPerformed

    private void btnDocEXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocEXActionPerformed
        model.setRowCount(0);
        Workbook wordbook;
        dangdocfile=1;
        JFileChooser jc=new JFileChooser();
        int vv=jc.showOpenDialog(jLabel4);
        if(vv==0){
            File link=jc.getSelectedFile();
            try{
                wordbook=Workbook.getWorkbook(link);
                Sheet sheet=wordbook.getSheet(0);
                int row=sheet.getRows();
                int col=sheet.getColumns();
                
                for(int i=1;i<row;++i){
                    Vector r=new Vector();
                    for(int j=0;j<col;++j)
                    {
                        Cell cell=sheet.getCell(j, i);
                        r.add(cell.getContents());
                    }
                    model.addRow(r);
                }
                tbl.setModel(model);
                wordbook.close();
            }
            catch (IOException ex) {
                Logger.getLogger(Kho.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BiffException ex) {
                Logger.getLogger(Kho.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDocEXActionPerformed

