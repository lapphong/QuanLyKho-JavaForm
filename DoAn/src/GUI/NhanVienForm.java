    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String b=txtTimKiem.getText();
        boolean check=false;
        for(int i=0;i<bus.lsk.size();i++){
            if(bus.lsk.get(i).getMaNV().toLowerCase().equals(b.toLowerCase())){
                check=true;
                model.setRowCount(0);
                model.addRow(new Object[]{
                    bus.lsk.get(i).getMaNV(), bus.lsk.get(i).getTenNV(), bus.lsk.get(i).getGioitinh(), bus.lsk.get(i).getChucvu(),bus.lsk.get(i).getCMND(),bus.lsk.get(i).getDiaChi()
                });
                break;
            }
            else if(bus.lsk.get(i).getTenNV().toLowerCase().equals(b.toLowerCase())){
                check=true;
                model.setRowCount(0);
                model.addRow(new Object[]{
                    bus.lsk.get(i).getMaNV(), bus.lsk.get(i).getTenNV(), bus.lsk.get(i).getGioitinh(), bus.lsk.get(i).getChucvu(),bus.lsk.get(i).getCMND(),bus.lsk.get(i).getDiaChi()
                });
                break;
            }
        }
        if(check==false){
            JOptionPane.showMessageDialog(rootPane, "Không có MãSP hay TênSP trong bảng nhóe !");
            model.setRowCount(0);
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int row=tbl.getSelectedRow();
        txtMaNV.setText(tbl.getModel().getValueAt(row, 0).toString());
        txtMaNV.setEnabled(false);
        txtTenNV.setText(tbl.getModel().getValueAt(row, 1).toString());
        if(tbl.getModel().getValueAt(row, 2).toString().equals("Nam")){
            cmbGioiTinh.setSelectedIndex(0);
        }
        else if(tbl.getModel().getValueAt(row, 2).toString().equals("Nữ")){
            cmbGioiTinh.setSelectedIndex(1);
        }
        else if(tbl.getModel().getValueAt(row, 2).toString().equals("bê đê")){
            cmbGioiTinh.setSelectedIndex(2);
        }
        
        if(tbl.getModel().getValueAt(row, 3).toString().equals("Quản lý"))
            cmbChuVu.setSelectedIndex(0);
        else if(tbl.getModel().getValueAt(row, 3).toString().equals("Làm công"))
            cmbChuVu.setSelectedIndex(1);
        else if(tbl.getModel().getValueAt(row, 3).toString().equals("Bảo vệ"))
            cmbChuVu.setSelectedIndex(2);
        
        txtCMND.setText(tbl.getModel().getValueAt(row, 4).toString());
        txtDiaChi.setText(tbl.getModel().getValueAt(row, 5).toString());
    }//GEN-LAST:event_tblMouseClicked

