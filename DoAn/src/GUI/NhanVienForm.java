    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien s = new NhanVien();
        NhanVienBUS bus=new NhanVienBUS();
        
        TaiKhoanBUS tk=new TaiKhoanBUS();
        tk.DocTaiKhoan();
        boolean check=true;
        String b=txtMaNV.getText();
        if(!bus.KiemTraTrungMaNV(b) ){
            check=false;
            JOptionPane.showMessageDialog(rootPane, "Trùng  Mã NV ");
            txtMaNV.requestFocus();
        }
        s.setMaNV(b);
        
        s.setTenNV(txtTenNV.getText());
        s.setGioitinh(cmbGioiTinh.getSelectedItem().toString());
        s.setChucvu(cmbChuVu.getSelectedItem().toString());
        
        if(txtCMND.getDocument().getLength()<10){
            check=false;
            JOptionPane.showMessageDialog(rootPane, "CMND phải có ít nhất 10 số!");
            txtCMND.requestFocus();
        }
        
        String p=txtCMND.getText();
        try {
            Integer.parseInt(p);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "CMND phải là số");
            check=false;
            txtCMND.requestFocus();
        }
        s.setCMND(p);
        s.setDiaChi(txtDiaChi.getText());

        if(check==true){
            try {
                if(bus.ThemNV(s)==true){
                    bus.showResult(model);
                    JOptionPane.showMessageDialog(rootPane, "Thêm NV thành công!");

                    txtMaNV.setText("");
                    txtTenNV.setText("");
                    cmbGioiTinh.setSelectedIndex(0);
                    cmbChuVu.setSelectedIndex(0);
                    txtCMND.setText("");
                    txtDiaChi.setText("");
                    txtMaNV.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Thêm không thành cong ! Chắc là sai 1 chỗ gì đó rầu");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row=tbl.getSelectedRow();
        boolean check1=true;
        if(row == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn dòng để sủa");
            check1=false;
        }
        if(check1==true){
            NhanVien std = bus.lsk.get(row);
            if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa?", "WARNING",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    bus.xoavitrids(row);
                    bus.XoaNV(std);
                    txtMaNV.setText("");
                    txtTenNV.setText("");
                    cmbGioiTinh.setSelectedIndex(0);
                    cmbChuVu.setSelectedIndex(0);
                    txtCMND.setText("");
                    txtDiaChi.setText("");
                    txtMaNV.requestFocus();
                    txtMaNV.setEnabled(true);
                } catch (SQLException ex) {
                    Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
                }
                model.removeRow(row);
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Lần sau nghĩ kỹ r hãy ấn nha mạy !!!");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        NhanVien s = new NhanVien();
        boolean check=true;

        int row=tbl.getSelectedRow();
        boolean check1=true;
        if(row == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn dòng để sủa");
            check1=false;
        }
        if(check1==true){
            s.setTenNV(txtTenNV.getText());
            s.setMaNV(txtMaNV.getText());
            
            s.setGioitinh(cmbGioiTinh.getSelectedItem().toString());
            s.setChucvu(cmbChuVu.getSelectedItem().toString());
            if(txtCMND.getDocument().getLength()<10){
                check=false;
                JOptionPane.showMessageDialog(rootPane, "CMND phải có ít nhất 10 số!");
                txtCMND.requestFocus();
            }

            String p=txtCMND.getText();
            try {
                Integer.parseInt(p);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "CMND phải là số");
                check=false;
                txtCMND.requestFocus();
            }
            s.setCMND(p);
            s.setDiaChi(txtDiaChi.getText());

            if(check==true){
                try {
                    if(bus.SuaNV(s)==true){
                        model.setRowCount(0);
                        bus.DocNV();
                        bus.showTable(model);
                        txtMaNV.setText("");
                        txtTenNV.setText("");
                        cmbGioiTinh.setSelectedIndex(0);
                        cmbChuVu.setSelectedIndex(0);
                        txtCMND.setText("");
                        txtMaNV.setEnabled(true);
                        txtDiaChi.setText("");
                        txtMaNV.requestFocus();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(rootPane, "Edit thất bại");
                }

            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

